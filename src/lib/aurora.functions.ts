const MessageSchema = z.object({
  role: z.enum(["user", "assistant", "system"]),
  content: z.string().min(1).max(4000),
});

export const auroraChat = createServerFn({ method: "POST" })
  .inputValidator(
    z.object({
      messages: z.array(MessageSchema).min(1).max(40),
      lang: z.string().max(10).optional(), // язык сайта гостя (ru/en/zh)
    })
  )
  .handler(async function* ({ data }) {
    // Антиспам/защита кредитов: не более 20 запросов за минуту с одного IP.
    if (!(await checkRateLimit("aurora", { max: 20, windowSec: 60 }))) {
      yield { delta: "Слишком много сообщений. Пожалуйста, подождите минуту." };
      return;
    }

    // FAQ-перехват: типовые вопросы (трансфер, питание, заезд…) отвечаем
    // готовым текстом на языке гостя — без обращения к Gemini (экономия токенов).
    const uiLang = normalizeLang(data.lang);
    const lastUser = [...data.messages].reverse().find((m) => m.role === "user");
    const faq = lastUser ? matchFaq(lastUser.content, data.lang) : null;
    if (faq) {
      yield { delta: faq };
      return;
    }

    // Gemini API через OpenAI-совместимый эндпоинт (тот же формат стриминга).
    // Ключ берётся в Google AI Studio (https://aistudio.google.com/apikey).
    const apiKey = process.env.GEMINI_API_KEY;
    if (!apiKey) {
      yield { delta: "Сервис AI временно недоступен." };
      return;
    }
    const model = process.env.GEMINI_MODEL ?? "gemini-2.5-flash";

    const upstream = await fetch(
      "https://generativelanguage.googleapis.com/v1beta/openai/chat/completions",
      {
        method: "POST",
        headers: {
          Authorization: `Bearer ${apiKey}`,
          "Content-Type": "application/json",
        },
        body: JSON.stringify({
          model,
          stream: true,
          messages: [
            { role: "system", content: SYSTEM_PROMPT },
            ...(uiLang
              ? [{
                  role: "system",
                  content: `Язык сайта, который выбрал гость: ${LANG_NAME[uiLang]}. Отвечай на этом языке, если в сообщении гостя нет явно другого языка.`,
                }]
              : []),
            ...data.messages,
          ],
        }),
      }
    );

    if (!upstream.ok || !upstream.body) {
      if (upstream.status === 429) {
        yield { delta: "Слишком много запросов. Попробуйте через минуту." };
      } else if (upstream.status === 402) {
        yield {
          delta:
            "Закончились кредиты AI. Свяжитесь, пожалуйста, с ресепшн: +7 (914) 994-57-57.",
        };
      } else {
        yield { delta: "Не удалось получить ответ. Попробуйте ещё раз." };
      }
      return;
    }

    const reader = upstream.body.pipeThrough(new TextDecoderStream()).getReader();
    let buffer = "";
    while (true) {
      const { done, value } = await reader.read();
      if (done) break;
      buffer += value;
      let idx;
      while ((idx = buffer.indexOf("\n")) !== -1) {
        const line = buffer.slice(0, idx).trim();
        buffer = buffer.slice(idx + 1);
        if (!line.startsWith("data:")) continue;
        const payload = line.slice(5).trim();
        if (payload === "[DONE]") return;
        try {
          const json = JSON.parse(payload);
          const delta = json.choices?.[0]?.delta?.content;
          if (delta) yield { delta };
        } catch {
          // ignore parse errors on partial lines
        }
      }
    }
  });
